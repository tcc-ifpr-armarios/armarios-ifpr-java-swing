    private void salvar() throws HeadlessException {
        try {
            ServidorServico.inserir(estudante);
            JOptionPane.showMessageDialog(this, MensagemUtil.SERVIDOR_INSERCAO_SUCESSO);
            fecharFormulario();
        } catch (ServidorException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), MensagemUtil.TITULO_ERRO_FATAL, JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, MensagemUtil.SERVIDOR_INSERCAO_ERRO_PADRAO, MensagemUtil.TITULO_ERRO_FATAL, JOptionPane.ERROR_MESSAGE);
        }
    }

    private void atualizar() {
        try {
            ServidorServico.atualizar(estudante);
            JOptionPane.showMessageDialog(this, MensagemUtil.SERVIDOR_ATUALIZACAO_SUCESSO);
            fecharFormulario();
        } catch (ServidorException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), MensagemUtil.TITULO_ERRO_FATAL, JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, MensagemUtil.SERVIDOR_INSERCAO_ERRO_PADRAO, MensagemUtil.TITULO_ERRO_FATAL, JOptionPane.ERROR_MESSAGE);
        }
    }

    private void fecharFormulario() {
        this.indexServidorPanelUI.init();
        this.dispose();
    }

