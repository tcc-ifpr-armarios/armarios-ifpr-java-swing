    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!Arrays.equals(txtConfirmaSenha.getPassword(), txtSenha.getPassword())) {
            JOptionPane.showMessageDialog(this, MensagemUtil.SENHAS_NAO_CONFEREM);
            return;
        }

        this.estudante.setNome(txtNome.getText());
        this.estudante.setSobrenome(txtSobrenome.getText());
        this.estudante.setSiape(txtSiape.getText());
        this.estudante.setTelefone(txtTelefone.getText());
        this.estudante.setEmail(txtEmail.getText());
        this.estudante.setAtivo(ckbAtivo.isSelected());

        char[] caracteresSenha = this.txtSenha.getPassword();
        String senha = new String(caracteresSenha);
        if (!senha.equals(this.estudante.getSenha())) {
            this.estudante.setSenha(senha);
        }

        if (estaAtualizando)
            atualizar();
        else
            salvar();
    }//GEN-LAST:event_btnSalvarActionPerformed

